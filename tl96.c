bool rgb_matrix_indicators_advanced_user(uint8_t led_min, uint8_t led_max)
{
    if (host_keyboard_led_state().caps_lock) 
    {   //CapsLock锁定时强制33号LED的颜色
        RGB_MATRIX_INDICATOR_SET_COLOR(33, 185, 0, 0);
    } 
    else 
    {   //CapsLock解锁时强制33号LED的颜色
    }

    if (host_keyboard_led_state().num_lock) 
    {   //NumLock锁定时强制65号LED的颜色
    } 
    else 
    {   //NumLock解锁时强制65号LED的颜色
        RGB_MATRIX_INDICATOR_SET_COLOR(65, 185, 0, 0);
    }

    switch (get_highest_layer(layer_state)) 
    {
        case 0:
            break;
        case 1:
                rgb_matrix_set_color(0, 0,175,175);

                rgb_matrix_set_color(82, 0,0,175);
                rgb_matrix_set_color(83, 0,90,175);
                rgb_matrix_set_color(84, 0,175,90);

                rgb_matrix_set_color(88, 90,0,90);
                rgb_matrix_set_color(89, 175,0,175);
                rgb_matrix_set_color(90, 90,0,90);

                rgb_matrix_set_color(91, 175,0,0);
                rgb_matrix_set_color(92, 175,90,0);
                rgb_matrix_set_color(93, 90,175,0);

                rgb_matrix_set_color(28, 0,90,90);
                rgb_matrix_set_color(29, 0,90,90);
                rgb_matrix_set_color(30, 0,90,90);
                rgb_matrix_set_color(45, 0,90,90);
            break;
        default:
            break;
    }
    return false;
}

