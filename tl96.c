bool rgb_matrix_indicators_advanced_user(uint8_t led_min, uint8_t led_max)
{
    if (host_keyboard_led_state().caps_lock) 
    {	//CapsLock锁定时强制0号LED的颜色
        RGB_MATRIX_INDICATOR_SET_COLOR(0, 185, 0, 0);
    } 
    else 
    {	//CapsLock解锁时强制0号LED的颜色
    }

    if (host_keyboard_led_state().num_lock) 
    {	//NumLock锁定时强制55号LED的颜色
    } 
    else 
    {	//NumLock解锁时强制55号LED的颜色
        RGB_MATRIX_INDICATOR_SET_COLOR(55, 185, 0, 0);
    }

    switch (get_highest_layer(layer_state)) 
    {
        case 0:
            break;
        case 1:
                rgb_matrix_set_color(4, 0,175,175);
                rgb_matrix_set_color(11, 0,175,175);

                rgb_matrix_set_color(55, 175,0,0);

                rgb_matrix_set_color(89, 175,0,0);
                rgb_matrix_set_color(90, 175,90,0);
                rgb_matrix_set_color(91, 90,175,0);

                rgb_matrix_set_color(86, 90,0,90);
                rgb_matrix_set_color(87, 175,0,175);
                rgb_matrix_set_color(88, 90,0,90);

                rgb_matrix_set_color(23, 0,90,90);
                rgb_matrix_set_color(6, 0,90,90);
                rgb_matrix_set_color(7, 0,90,90);
                rgb_matrix_set_color(8, 0,90,90);
            break;
        default:
            break;
    }
    return false;
}

